-- Use the SUM function to get the total amount grossed by all films.
SELECT SUM(gross)
FROM films;


/*
Get the average amount grossed by all films.
*/
SELECT AVG(gross)
FROM films;

/*
Get the amount grossed by the worst performing film.
*/
SELECT MIN(gross)
FROM films;


/*
Get the duration of the longest film.
*/
SELECT MAX(gross)
FROM films;



